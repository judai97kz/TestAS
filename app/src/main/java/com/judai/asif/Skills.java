package com.judai.asif;

public class Skills {
    private String skill;
    private int imgsk;

    public Skills(String skill, int imgsk) {
        this.skill = skill;
        this.imgsk = imgsk;
    }

    public String getSkill() {
        return skill;
    }

    public void setSkill(String skill) {
        this.skill = skill;
    }

    public int getImgsk() {
        return imgsk;
    }

    public void setImgsk(int imgsk) {
        this.imgsk = imgsk;
    }
}
